public class Key {
  private char output;
  private int length;

  public Key(char output, int length) {
    this.output = output;
    this.length = length;
  }

  public char getOutput() {
    return output;
  }

  public int getLength() {
    return length;
  }

  public void setOutput() {
    this.output = output;
  }

  public void setLength() {
    this.length = length;
  }
}
